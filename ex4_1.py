# Just a simple calculator

shelves = 6                             # the number of shelves in a room
books = 100                             # the number of books
books_per_shelf = books / shelves       # the average number of books per a single shelf

print("We have", shelves, "shelves in a room.")
print("We have", books, "books to put on the shelves.")
print("On average we have to put", books_per_shelf, "books on a single shelf")